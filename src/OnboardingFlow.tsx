// Плавно анимирует переход между шагами онбординга (кросс-фейд) и изменение высоты контейнера
function AnimatedStepContainer({ stepKey, children }: { stepKey: string; children: React.ReactNode }) {
  const innerRef = useRef<HTMLDivElement>(null);
  const [height, setHeight] = useState<number | undefined>(undefined);
  const [displayKey, setDisplayKey] = useState(stepKey);
  const [displayChildren, setDisplayChildren] = useState(children);
  const [visible, setVisible] = useState(true);

  useEffect(() => {
    const el = innerRef.current;
    if (!el) return;
    const ro = new ResizeObserver(([entry]) => setHeight(entry.contentRect.height));
    ro.observe(el);
    return () => ro.disconnect();
  }, []);

  // содержимое того же экрана меняется сразу, без кросс-фейда — только высота анимируется ниже
  useEffect(() => {
    if (stepKey === displayKey) setDisplayChildren(children);
  }, [children, stepKey, displayKey]);

  // смена экрана — плавный кросс-фейд
  useEffect(() => {
    if (stepKey === displayKey) return;
    setVisible(false);
    const t = setTimeout(() => {
      setDisplayChildren(children);
      setDisplayKey(stepKey);
      setVisible(true);
    }, 180);
    return () => clearTimeout(t);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [stepKey]);

  return (
    <div style={{ height, transition: 'height 0.25s ease', overflow: 'hidden' }}>
      <div
        ref={innerRef}
        style={{
          opacity: visible ? 1 : 0,
          transform: visible ? 'translateY(0)' : 'translateY(8px)',
          transition: 'opacity 0.18s ease, transform 0.18s ease',
        }}
      >
        {displayChildren}
      </div>
    </div>
  );
}


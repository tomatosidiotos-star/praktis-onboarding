// Экран 3А — Нет МЧД (офлайн-инструкция)
function ScreenNoMchd({ user, onDone, onBack }: {
  user: CurrentUser | null;
  onDone: () => void;
  onBack: () => void;
}) {
  const [copied, setCopied] = useState(false);

  const copyText = () => {
    if (!user) return;
    const text = `Добавьте, пожалуйста, пользователя:\nФИО: ${user.fullName}\nEmail: ${user.email}${user.phone ? `\nТелефон: ${user.phone}` : ''}`;
    navigator.clipboard.writeText(text).then(() => {
      setCopied(true);
      setTimeout(() => setCopied(false), 2000);
    });
  };

  return (
    <ScreenCard>
      <BackLink onClick={onBack} />
      <div>
        <ScreenTitle>Свяжитесь с администратором вашей компании</ScreenTitle>
        <ScreenSubtitle style={{ marginTop: 8 }}>
          Чтобы получить доступ, попросите администратора компании в Praktis добавить вас
          по имени или email. Сделать это можно по телефону, в почте или мессенджере.
          Как только вас добавят — доступ к модулям откроется автоматически.
        </ScreenSubtitle>
      </div>

      <div
        style={{
          background: C.bgPage,
          borderRadius: R.lg,
          padding: 20,
          display: 'flex',
          flexDirection: 'column',
          gap: 12,
        }}
      >
        <div style={{ ...font(13, 500), color: C.textMuted }}>Ваши данные для передачи администратору</div>
        {user && (
          <div style={{ display: 'flex', flexDirection: 'column', gap: 4 }}>
            <span style={{ ...font(14), color: C.textPrimary }}>{user.fullName}</span>
            <span style={{ ...font(14), color: C.textSecondary }}>{user.email}</span>
            {user.phone && <span style={{ ...font(14), color: C.textSecondary }}>{user.phone}</span>}
          </div>
        )}
        <SecondaryButton onClick={copyText} style={{ alignSelf: 'flex-start' }}>
          {copied ? <><IconCheck color="#282828" size={16} />&nbsp;Скопировано</> : <><IconCopy color="#282828" size={16} />&nbsp;Скопировать данные</>}
        </SecondaryButton>
      </div>

      <div style={{ display: 'flex', justifyContent: 'flex-end' }}>
        <PrimaryButton onClick={onDone}>Понятно</PrimaryButton>
      </div>
    </ScreenCard>
  );
}


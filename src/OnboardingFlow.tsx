export default function OnboardingFlow({
  onComplete,
}: {
  /**
   * Вызывается когда онбординг завершён.
   * TODO: передать сюда навигацию — напр. navigate('/') или router.push('/')
   */
  onComplete: (tab: 'showcase' | 'catalog-is') => void;
}) {
  const [step, setStep] = useState<OnboardingStep>('entry');
  const [user, setUser] = useState<CurrentUser | null>(null);
  const [modules, setModules] = useState<PlatformModule[]>(FALLBACK_MODULES);

  // МЧД flow state
  const [joinRequestCompanyNames, setJoinRequestCompanyNames] = useState<string[]>([]);

  // УКЭП flow state
  const [createdCompanyNames, setCreatedCompanyNames] = useState<string[]>([]);
  const [ukepResults, setUkepResults] = useState<UkepVerifyResult[]>([]);

  useEffect(() => {
    // TODO: убрать .catch когда будет реальный API
    api.getCurrentUser().then(setUser).catch(() => {});
    api.getAvailableModules().then(setModules).catch(() => {});
  }, []);

  const handleMchdVerified = async (results: MchdVerifyResult[]) => {
    try {
      await Promise.all(results.map(c => api.submitJoinRequest(c.id)));
    } catch {
      // TODO: показать ошибку пользователю через message.error()
    }
    setJoinRequestCompanyNames(results.map(c => c.companyName));
    setStep('path-a-sent');
  };

  const handleUkepContinue = (results: UkepVerifyResult[]) => {
    setUkepResults(results);
    setStep('path-b-select-company');
  };

  const handleCompaniesCreate = async (selected: UkepVerifyResult[]) => {
    try {
      // TODO: создавать все выбранные компании, пока создаём первую
      await api.createCompany({ ukepId: selected[0].inn, companyName: selected[0].companyName, inn: selected[0].inn });
      setCreatedCompanyNames(selected.map(r => r.companyName));
      setStep('path-b-created');
    } catch {
      // TODO: показать ошибку пользователю
    }
  };

  const renderStep = () => {
    switch (step) {
      case 'entry':
        return (
          <ScreenEntry
            onJoin={() => setStep('path-a-mchd-choice')}
            onCreate={() => setStep('path-b-upload-ukep')}
          />
        );

      case 'path-a-mchd-choice':
        return (
          <ScreenMchdChoice
            onHasMchd={() => setStep('path-a-upload-mchd')}
            onNoMchd={() => setStep('path-a-no-mchd')}
            onBack={() => setStep('entry')}
          />
        );

      case 'path-a-no-mchd':
        return (
          <ScreenNoMchd
            user={user}
            onDone={() => setStep('final')}
            onBack={() => setStep('path-a-mchd-choice')}
          />
        );

      case 'path-a-upload-mchd':
        return (
          <ScreenUploadMchd
            onSuccess={handleMchdVerified}
            onBack={() => setStep('path-a-mchd-choice')}
          />
        );

      case 'path-a-sent':
        return (
          <ScreenJoinRequestSent
            companyNames={joinRequestCompanyNames.length > 0 ? joinRequestCompanyNames : ['вашей компании']}
            onViewModules={() => onComplete('catalog-is')}
          />
        );

      case 'path-b-upload-ukep':
        return (
          <ScreenUploadUkep
            onContinue={handleUkepContinue}
            onBack={() => setStep('entry')}
            onSkip={() => {
              setUkepResults([
                { companyName: 'ООО "Ромашка"', inn: '7700000001', directorName: 'Иванов Иван Иванович' },
                { companyName: 'ООО "СтройПроект"', inn: '7712345678', directorName: 'Иванов Иван Иванович' },
              ]);
              setStep('path-b-select-company');
            }}
          />
        );

      case 'path-b-select-company':
        return (
          <ScreenSelectCompany
            companies={ukepResults}
            onBack={() => setStep('path-b-upload-ukep')}
            onSkip={() => { setCreatedCompanyNames(['ООО "Ромашка"', 'ООО "СтройПроект"']); setStep('path-b-created'); }}
            onSubmit={handleCompaniesCreate}
          />
        );

      case 'path-b-created':
        return (
          <ScreenCompanyCreated
            companyNames={createdCompanyNames.length > 0 ? createdCompanyNames : ['ООО Демо']}
            onViewModules={() => onComplete('catalog-is')}
          />
        );

      case 'final':
        return (
          <ScreenFinalModules
            modules={modules}
            onBack={() => setStep('path-a-sent')}
          />
        );
    }
  };

  return (
    <div
      style={{
        height: '100vh',
        background: C.bgPage,
        display: 'flex',
        flexDirection: 'column',
        boxSizing: 'border-box',
      }}
    >
      <Header user={user} />

      <main
        style={{
          flex: 1,
          overflowY: 'auto',
          display: 'flex',
          flexDirection: 'column',
          justifyContent: 'center',
          maxWidth: 800,
          width: '100%',
          margin: '0 auto',
          padding: '24px 0',
        }}
      >
        <AnimatedStepContainer stepKey={step}>{renderStep()}</AnimatedStepContainer>
      </main>

      <Footer />
    </div>
  );
}

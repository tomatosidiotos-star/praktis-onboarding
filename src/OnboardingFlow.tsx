function ScreenSelectCompany<T extends { companyName: string; inn: string }>({
  companies, onBack, onSkip, onSubmit,
  title = 'Выберите компании',
  subtitleText,
  actionLabel = (n: number) => (n > 1 ? `Создать ${n} компании` : 'Создать компанию'),
}: {
  companies: T[];
  onBack: () => void;
  onSkip: () => void;
  onSubmit: (selected: T[]) => void;
  title?: string;
  subtitleText?: string;
  actionLabel?: (count: number) => string;
}) {
  const [selected, setSelected] = useState<Set<number>>(new Set());
  const [hovered, setHovered] = useState<number | null>(null);

  const toggle = (i: number) =>
    setSelected(prev => {
      const next = new Set(prev);
      next.has(i) ? next.delete(i) : next.add(i);
      return next;
    });

  const selectedList = companies.filter((_, i) => selected.has(i));

  return (
    <ScreenCard>
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center' }}>
        <BackLink onClick={onBack} />
        <SecondaryButton onClick={onSkip} style={{ ...font(13), color: C.textMuted }}>Пропустить</SecondaryButton>
      </div>
      <div>
        <ScreenTitle>{title}</ScreenTitle>
        <ScreenSubtitle style={{ marginTop: 8 }}>
          {subtitleText ?? `Мы нашли ${companies.length === 1 ? 'организацию' : 'организации'} в загруженных подписях. Отметьте те, которые хотите зарегистрировать в Praktis.`}
        </ScreenSubtitle>
      </div>

      <div style={{ display: 'flex', flexDirection: 'column', gap: 8 }}>
        {companies.map((company, i) => {
          const isSelected = selected.has(i);
          const isHovered = hovered === i;
          const borderColor = isSelected || isHovered ? C.primary : '#EAF0F5';
          return (
            <button
              key={i}
              onClick={() => toggle(i)}
              onMouseEnter={() => setHovered(i)}
              onMouseLeave={() => setHovered(null)}
              style={{
                padding: '14px 16px',
                borderRadius: R.md,
                border: `1.5px solid ${borderColor}`,
                background: isSelected ? '#f0f6ff' : C.white,
                cursor: 'pointer',
                textAlign: 'left',
                display: 'flex',
                justifyContent: 'space-between',
                alignItems: 'center',
                transition: 'border-color 0.15s, background 0.15s',
              }}
            >
              <div>
                <div style={{ ...font(14, 600), color: C.textPrimary }}>{company.companyName}</div>
                <div style={{ ...font(12), color: C.textMuted, marginTop: 2 }}>ИНН {company.inn}</div>
              </div>
              <div style={{
                width: 18, height: 18, borderRadius: '50%',
                border: `2px solid ${isSelected ? C.primary : C.borderDefault}`,
                background: isSelected ? C.primary : 'transparent',
                flexShrink: 0,
                display: 'flex', alignItems: 'center', justifyContent: 'center',
              }}>
                {isSelected && (
                  <svg width="10" height="10" viewBox="0 0 10 10" fill="none">
                    <path d="M2 5L4 7L8 3" stroke="white" strokeWidth="1.5" strokeLinecap="round" strokeLinejoin="round" />
                  </svg>
                )}
              </div>
            </button>
          );
        })}
      </div>

      <div style={{ display: 'flex', justifyContent: 'flex-end' }}>
        <PrimaryButton onClick={() => onSubmit(selectedList)} disabled={selectedList.length === 0}>
          {actionLabel(selectedList.length)}
        </PrimaryButton>
      </div>
    </ScreenCard>
  );
}


// Зона загрузки файла
function UploadZone({
  accept,
  onFile,
  hint,
  disabled,
}: {
  accept: string;
  onFile: (file: File) => void;
  hint: string;
  disabled?: boolean;
}) {
  const inputRef = useRef<HTMLInputElement>(null);
  const [dragging, setDragging] = useState(false);
  const [hovered, setHovered] = useState(false);

  const handleDrop = (e: React.DragEvent) => {
    e.preventDefault();
    setDragging(false);
    if (disabled) return;
    const file = e.dataTransfer.files[0];
    if (file) onFile(file);
  };

  const active = !disabled && (dragging || hovered);

  return (
    <div
      onClick={() => !disabled && inputRef.current?.click()}
      onMouseEnter={() => !disabled && setHovered(true)}
      onMouseLeave={() => setHovered(false)}
      onDragOver={(e) => { e.preventDefault(); if (!disabled) setDragging(true); }}
      onDragLeave={() => setDragging(false)}
      onDrop={handleDrop}
      style={{
        border: `1px dashed ${disabled ? '#e5e5e5' : active ? '#8ab5fa' : C.borderDefault}`,
        borderRadius: R.sm,
        background: active ? '#f7faff' : C.white,
        padding: 16,
        cursor: disabled ? 'not-allowed' : 'pointer',
        transition: 'border-color 0.15s, background 0.15s',
        display: 'flex',
        flexDirection: 'column',
        alignItems: 'center',
        gap: 8,
      }}
    >
      <input
        ref={inputRef}
        type="file"
        accept={accept}
        style={{ display: 'none' }}
        onChange={(e) => { const f = e.target.files?.[0]; if (f) onFile(f); }}
      />
      <IconUpload color={disabled ? '#cbcbcb' : active ? C.primary : C.textPrimary} size={32} />
      <div style={{ display: 'flex', flexDirection: 'column', alignItems: 'center', gap: 4 }}>
        <div style={{ ...font(18, 500), color: disabled ? '#cbcbcb' : C.textPrimary, lineHeight: 1.2 }}>
          Загрузить файлы
        </div>
        <div style={{ ...font(12), color: C.textMuted, lineHeight: 1.4, textAlign: 'center' }}>{hint}</div>
      </div>
    </div>
  );
}

